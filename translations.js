// =============================================================================
// Language Switcher Logic
// =============================================================================

function getCurrentLang() {
  return localStorage.getItem('adir-lang') || 'en';
}

function t(key) {
  var lang = getCurrentLang();
  return (translations[lang] && translations[lang][key]) || (translations.en && translations.en[key]) || key;
}

function applyTranslations() {
  var lang = getCurrentLang();
  var isHe = lang === 'he';

  // Set HTML attributes
  document.documentElement.lang = lang;
  document.documentElement.dir = isHe ? 'rtl' : 'ltr';

  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var val = t(key);
    if (val) el.innerHTML = val;
  });

  // Update all elements with data-i18n-placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    var val = t(key);
    if (val) el.placeholder = val;
  });

  // Update lang toggle button text
  var toggleBtn = document.getElementById('langToggle');
  if (toggleBtn) {
    toggleBtn.textContent = isHe ? 'EN' : 'עב';
  }
}

function toggleLanguage() {
  var current = getCurrentLang();
  var next = current === 'en' ? 'he' : 'en';
  localStorage.setItem('adir-lang', next);
  applyTranslations();

  // Fire custom event so page-specific JS can react
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: next } }));
}

// Apply on DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', applyTranslations);
} else {
  applyTranslations();
}
